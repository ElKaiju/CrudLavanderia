    public void limpiarCajas(){
        txtFecha.setText("");
        txtCliente.setText("");
        txtPrenda.setText("");
        txtTotal.setText("");
    }
    
    public void insertarDatos(){
        try {
            
            String SQL = "INSERT INTO Venta_Boleta (FechaBoleta, NombreCliente, NumeroPrendas, ValorVenta, MetodoPago) VALUES (?,?,?,?,?)";
            
            PreparedStatement pst = con.prepareStatement(SQL);
            
            pst.setString(1, txtFecha.getText());
            pst.setString(2, txtCliente.getText());
            pst.setInt(3, Integer.parseInt(txtPrenda.getText()));
            pst.setInt(4, Integer.parseInt(txtTotal.getText()));
            
            int formaPago = cmbPago.getSelectedIndex();
            pst.setString(5, cmbPago.getItemAt(formaPago));
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Venta registrada exitosamente.");
            
            
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error al agregar venta: "+e.getMessage());
            
        }
    }
    
