    public void mostrarDatos(){
        String[] titulos = {"Nro Boleta ","Fecha ", "Nombre Cliente ", "Nro Prendas ", "Valor Total ", "Método de Pago "};
        String[] registro = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        
        String SQL = "SELECT * FROM Venta_Boleta";
        
        try {
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            while (rs.next()){
                
                registro[0] = rs.getString("NumeroBoleta");
                registro[1] = rs.getString("FechaBoleta");
                registro[2] = rs.getString("NombreCliente");
                registro[3] = rs.getString("NumeroPrendas");
                registro[4] = rs.getString("ValorVenta");
                registro[5] = rs.getString("MetodoPago");
                
                modelo.addRow(registro);
                   
            }
            
            tablaContenido.setModel(modelo);
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al insertar datos: "+e.getMessage());
        }
    }
    
